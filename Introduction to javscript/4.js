let str = "Hello";
// length: Returns the length of a string.
console.log(str.length); 

// toUpperCase(): Converts a string to Uppercase.
console.log(str.toUpperCase()); // Output: "HELLO"

// toLowerCase(): Converts a string to lowercase.
console.log(str.toUpperCase()); // Output: "HELLO"

// substring(start, end): Returns a part of the string between the specified indices.
console.log(str.substring(1, 4)); // Output: "ell"


// split(delimiter): Splits a string into an array based on a delimiter.
let sentence = "Hello World";
console.log(sentence.split(" ")); // Output: ["Hello", "World"]


//trim(): Removes whitespace from both ends of a string.
let strWithSpaces = "   Hello   ";
console.log(strWithSpaces.trim()); // Output: "Hello"

